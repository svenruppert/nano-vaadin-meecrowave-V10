package org.rapidpm.vaadin.helloworld.server;

import com.vaadin.flow.component.Composite;
import com.vaadin.flow.component.button.Button;
import com.vaadin.flow.component.html.Div;
import com.vaadin.flow.component.html.Label;
import com.vaadin.flow.component.orderedlayout.VerticalLayout;
import com.vaadin.flow.router.Route;
import com.vaadin.flow.theme.Theme;
import com.vaadin.flow.theme.lumo.Lumo;

@Route("")
@Theme(value = Lumo.class, variant = Lumo.LIGHT)
public class HelloVaadinV10 extends Composite<Div> {

  public HelloVaadinV10() {
    final VerticalLayout layout = new VerticalLayout();
    layout.add(new Button("click me") {
      {
        addClickListener(event -> layout.add(new Label("clicked again")));
      }
    });
    getContent().add(layout);
  }
}
